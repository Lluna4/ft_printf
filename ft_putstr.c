#include "ft_printf.h"

int ft_putstr(char *s)
{
    int ret;

    ret = 0;
    if (!s)
        s = "(null)";
    while (*s)
    {
        ret++;
        ft_putchar(*s);
        s++;
    }
    return (ret);
}